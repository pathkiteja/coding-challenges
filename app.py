# API Endpoint to Fetch Random Questions
@app.route('/get_questions', methods=['POST'])
def get_questions():
    data = request.json
    language = data.get('language')
    difficulty = data.get('difficulty')
    num_questions = int(data.get('num_questions', 5))

    if language not in question_bank or difficulty not in question_bank[language]:
        return jsonify({"error": "Invalid selection"}), 400

    questions = random.sample(
        question_bank[language][difficulty],
        min(num_questions, len(question_bank[language][difficulty]))
    )

    return jsonify({"questions": questions})

# API Endpoint to Fetch All Questions for Selected Language & Difficulty
@app.route('/get_all_questions', methods=['POST'])
def get_all_questions():
    data = request.json
    language = data.get('language')
    difficulty = data.get('difficulty')

    if language not in question_bank or difficulty not in question_bank[language]:
        return jsonify({"error": "Invalid selection"}), 400

    selected_questions = question_bank[language][difficulty]

    return jsonify({"questions": selected_questions})

